const countOnly = function(allItems, itemsToCount) {
  const results = {}

  for (const item of allItems) {
    //Inside the loop, add a counter for each item

    //Then set a property with that string key to:

    //The value that was already there with 1 added to it
    if (itemsToCount[item]) {
      if (results[item]) {
        results[item] += 1;
      } else {
        results[item] = 1;
      }
    }
  }
  return results; 

};

